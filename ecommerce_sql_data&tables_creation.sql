IF NOT EXISTS (SELECT * FROM sys.databases WHERE name = 'ecommerce_db')
BEGIN
    CREATE DATABASE ecommerce_db;
END
GO

USE ecommerce_db;
GO

-- Create tables
CREATE TABLE Category (
    categoryID INT PRIMARY KEY,
    Name NVARCHAR(100) NOT NULL,
    Discription NVARCHAR(MAX)
);
GO

CREATE TABLE [User] (
    UserID INT PRIMARY KEY,
    First_name NVARCHAR(50) NOT NULL,
    Last_name NVARCHAR(50) NOT NULL,
    Email NVARCHAR(100) UNIQUE NOT NULL,
    password_hash NVARCHAR(255) NOT NULL,
    phone NVARCHAR(20),
    gender NVARCHAR(10) NOT NULL CHECK (gender IN ('male', 'female', 'other')),
    join_date DATETIME NOT NULL
);
GO

CREATE TABLE Product (
    productID INT PRIMARY KEY,
    Name NVARCHAR(200) NOT NULL,
    Discription NVARCHAR(MAX),
    price DECIMAL(10,2) NOT NULL,
    Brand NVARCHAR(100),
    stock INT NOT NULL DEFAULT 0,
    discount_percentage FLOAT DEFAULT 0,
    category_ID INT,
    FOREIGN KEY (category_ID) REFERENCES Category(categoryID)
);
GO

CREATE TABLE Address (
    Address_id INT PRIMARY KEY,
    street NVARCHAR(200) NOT NULL,
    city NVARCHAR(100) NOT NULL,
    state NVARCHAR(100) NOT NULL,
    country NVARCHAR(100) NOT NULL,
    address_type NVARCHAR(10) NOT NULL CHECK (address_type IN ('shipping', 'billing')),
    user_id INT,
    FOREIGN KEY (user_id) REFERENCES [User](UserID)
);
GO

CREATE TABLE [Order] (
    orderID INT PRIMARY KEY,
    Status NVARCHAR(20) NOT NULL CHECK (Status IN ('pending', 'processing', 'shipped', 'delivered', 'cancelled')),
    order_created_at DATETIME NOT NULL,
    order_completed_at DATETIME,
    shipping_cost DECIMAL(10,2) NOT NULL,
    user_id INT,
    shipping_address_id INT,
    FOREIGN KEY (user_id) REFERENCES [User](UserID),
    FOREIGN KEY (shipping_address_id) REFERENCES Address(Address_id)
);
GO

CREATE TABLE Payment (
    paymentID INT PRIMARY KEY,
    amount DECIMAL(10,2) NOT NULL,
    payment_date DATETIME NOT NULL,
    status NVARCHAR(20) NOT NULL CHECK (status IN ('pending', 'completed', 'failed', 'refunded')),
    method NVARCHAR(20) NOT NULL CHECK (method IN ('credit_card', 'debit_card', 'paypal', 'cash', 'bank_transfer')),
    Order_id INT UNIQUE,
    FOREIGN KEY (Order_id) REFERENCES [Order](orderID)
);
GO

CREATE TABLE OrderDetails (
    order_id INT,
    product_id INT,
    quantity INT NOT NULL,
    unit_price DECIMAL(10,2) NOT NULL,
    PRIMARY KEY (order_id, product_id),
    FOREIGN KEY (order_id) REFERENCES [Order](orderID),
    FOREIGN KEY (product_id) REFERENCES Product(productID)
);
GO

CREATE TABLE Review (
    ReviewID INT PRIMARY KEY,
    rating FLOAT NOT NULL CHECK (rating >= 0 AND rating <= 5),
    comment NVARCHAR(MAX),
    review_date DATETIME NOT NULL,
    Product_id INT,
    User_id INT,
    Order_id INT,
    CONSTRAINT UQ_User_Product UNIQUE (User_id, Product_id),
    FOREIGN KEY (Product_id) REFERENCES Product(productID),
    FOREIGN KEY (User_id) REFERENCES [User](UserID),
    FOREIGN KEY (Order_id) REFERENCES [Order](orderID)
);
GO

CREATE TABLE [Return] (
    returnID INT PRIMARY KEY,
    refund_amount DECIMAL(10,2) NOT NULL,
    return_date DATETIME NOT NULL,
    reason NVARCHAR(MAX),
    returned_quantity INT NOT NULL,
    order_id INT,
    product_id INT,
    FOREIGN KEY (order_id, product_id) REFERENCES OrderDetails(order_id, product_id)
);
GO

